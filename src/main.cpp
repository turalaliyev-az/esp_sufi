// ==================== I2C Scanner ====================
void scanI2CDevices() {
  Serial.println("\nüîç I2C Taramasƒ± Ba≈ülƒ±yor...");
  byte error, address;
  int foundDevices = 0;

  for (address = 1; address < 127; address++) {
    Wire.beginTransmission(address);
    error = Wire.endTransmission();

    if (error == 0) {
      Serial.printf("‚úÖ I2C Cihaz Bulundu: 0x%02X", address);
      
      if (address == 0x40) Serial.println(" -> PCA9685 Servo S√ºr√ºc√º");
      else if (address == 0x68) Serial.println(" -> MPU6050 Gyro");
      else if (address == 0x76) Serial.println(" -> BME280 Sens√∂r");
      else if (address == 0x3C) Serial.println(" -> OLED Ekran");
      else if (address == 0x27) Serial.println(" -> LCD Ekran");
      else Serial.println(" -> Bilinmeyen Cihaz");
      
      foundDevices++;
    }
  }

  if (foundDevices == 0) {
    Serial.println("‚ùå Hi√ß I2C cihazƒ± bulunamadƒ±!");
  } else {
    Serial.printf("üìä Toplam %d I2C cihazƒ± bulundu\n", foundDevices);
  }
}

