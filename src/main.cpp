void pidSteering() {
  double frontLeft = sensors[0].distance;
  double frontRight = sensors[1].distance;

  // Eƒüer mesafeler max ise, farkƒ± 0 olarak kabul et (a√ßƒ±k alan)
  if (frontLeft >= MAX_DISTANCE && frontRight >= MAX_DISTANCE) {
    setLocalServo(90); // D√ºz git
    return;
  }

  double error = frontRight - frontLeft;  // Saƒü - Sol fark
  unsigned long now = millis();
  double timeChange = (double)(now - last_pid_time) / 1000.0;

  if (timeChange == 0) return;  // Zaman deƒüi≈üimi yoksa √ßƒ±k

  integral += error * timeChange;
  integral = constrain(integral, -MAX_INTEGRAL, MAX_INTEGRAL);  // Windup √∂nleme

  double derivative = (error - previous_error) / timeChange;

  double output = Kp * error + Ki * integral + Kd * derivative;

  previous_error = error;
  last_pid_time = now;

  int servoAngle = 90 + (int)output;
  servoAngle = constrain(servoAngle, 0, 180);

  setLocalServo(servoAngle);
  Serial.printf("üìê PID Steering: Error=%.2f, Output=%.2f, Angle=%d¬∞\n", error, output, servoAngle);
}

